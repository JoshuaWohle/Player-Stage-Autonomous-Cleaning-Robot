package robot;

/**
 * A listener interface for receiving collision events. Any object that is
 * interested in receiving collision events must implement this interface and
 * attach itself with the {@link Robot} using the {@link Robot.addCollisionListener} method.
 * 
 * @author mayank
 * @see Controller
 * 
 */
public interface CollisionListener {
	/**
	 * A method called whenever the robot has collided
	 * @param angle the angle at which the robot has collided
	 */
	public void collided(int angle);
}
